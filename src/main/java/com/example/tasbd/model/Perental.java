package com.example.tasbd.model;

public class Perental {
    private String id_perental;
    private String nama_perental;
    private String notelp_perental;
    private String alamat_perental;
    private String id_kaset;

    public String getid_perental() {
        return id_perental;
    }
    public void setid_perental(String id_perental) {
        this.id_perental = id_perental;
    }
    public String getnama_perental() {
        return nama_perental;
    }
    public void setnama_perental(String nama_perental) {
        this.nama_perental = nama_perental;
    }

    public String getnotelp_perental() {
        return notelp_perental;
    }
    public void setnotelp_perental(String notelp_perental) {
        this.notelp_perental = notelp_perental;
    }
    public String getalamat_perental() {
        return alamat_perental;
    }
    public void setalamat_perental(String alamat_perental) {
        this.alamat_perental = alamat_perental;
    }
    public String getid_kaset() {
        return id_kaset;
    }
    public void setid_kaset(String id_kaset) {
        this.id_kaset = id_kaset;
    }
}
